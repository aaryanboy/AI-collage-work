import numpy as np
a=np.array([1,2,3,4,5])

print(a)

# median

print('Median:', np.median(a))

# mean

print('Mean:', np.mean(a))

# standard deviation

print('Standard Deviation:', np.std(a))

# max

print('Max:', np.max(a))

# min

print('Min:', np.min(a))

# percentile

print('Percentile:', np.percentile(a, q=50))